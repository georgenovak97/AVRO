def set_language(lang):
    global _CURRENT
    lang = (lang or u"en").lower()
    if lang in _LANGUAGES:
        _CURRENT = lang


def get_language():
    return _CURRENT


def t(key, lang=None, **kwargs):
    """Translate key; optional ``lang`` override for ribbon metadata."""
    lng = lang or _CURRENT
    table = _STRINGS.get(lng, _STRINGS[u"ru"])
    text = table.get(key, _STRINGS[u"ru"].get(key, key))
    if kwargs:
        return text.format(**kwargs)
    return text


def init_from_config():
    try:
        import config
        set_language(config.get_ui_language())
    except Exception:
        set_language(u"en")
